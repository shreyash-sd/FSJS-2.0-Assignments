// Declare and assign variables of different data types
let myString = "Hello, World!";
let myBoolean = true;
let myUndefined = undefined;
let myNull = null;

// Display the value of each variable and its data type
console.log(`myString value: ${myString}, data type: ${typeof myString}`);
console.log(`myBoolean value: ${myBoolean}, data type: ${typeof myBoolean}`);
console.log(`myUndefined value: ${myUndefined}, data type: ${typeof myUndefined}`);
console.log(`myNull value: ${myNull}, data type: ${typeof myNull}`);
